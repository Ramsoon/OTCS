    	What do you know about Kogi State Polytechnic?
    </div>
    
    <div id="space"></div>
    
    <div style="text-indent:40px; text-align:justify">Kogi State Polytechnic was established in December 1992 by the 1st and 2cnd Executive Governor of Kogi State, Prince Abub akar Audu through an amended edict No. 6 of 1994.

The Polytechnic took off in January 1993 at the Government Science Secondary School, Lokoja and Osara Campus with Dr. Isa I.A. as the first Rector. The Polytechnic is located in Lokoja, Kogi State, Nigeria. It is owned and operated by Kogi State. As of 2007 it was accredited by the National Board for Technical Education (NBTE) to give certificates in Art and printing, Business, Engineering, Finance and Science Computing at the National Diploma and Higher National Diploma levels. In December 2009 the polytechnic presented 16 programs for accreditation to a visiting team of NBTE officials, mainly for the new schools of Engineering and Environmental Technology, Applied Science and Management Studies. The Rector said the school had to be able to offer core engineering and technology programs in order to fulfill its mandate.</div>
